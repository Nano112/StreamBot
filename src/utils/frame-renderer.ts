const GLYPH_W = 5;
const GLYPH_H = 7;

type RGB = [number, number, number];

export class FrameRenderer {
	readonly width: number;
	readonly height: number;
	private buf: Buffer;

	constructor(width: number, height: number) {
		this.width = width;
		this.height = height;
		this.buf = Buffer.alloc(width * height * 3);
	}

	/** Fill entire frame with a colour. */
	fill(r: number, g: number, b: number): this {
		for (let i = 0; i < this.buf.length; i += 3) {
			this.buf[i] = r; this.buf[i + 1] = g; this.buf[i + 2] = b;
		}
		return this;
	}

	/** Fill a rectangle. */
	rect(x: number, y: number, w: number, h: number, r: number, g: number, b: number, alpha = 1.0): this {
		for (let row = y; row < y + h && row < this.height; row++) {
			for (let col = x; col < x + w && col < this.width; col++) {
				if (row < 0 || col < 0) continue;
				const off = (row * this.width + col) * 3;
				if (alpha >= 1) {
					this.buf[off] = r; this.buf[off + 1] = g; this.buf[off + 2] = b;
				} else {
					this.buf[off]     = Math.round(this.buf[off]     * (1 - alpha) + r * alpha);
					this.buf[off + 1] = Math.round(this.buf[off + 1] * (1 - alpha) + g * alpha);
					this.buf[off + 2] = Math.round(this.buf[off + 2] * (1 - alpha) + b * alpha);
				}
			}
		}
		return this;
	}

	/** Draw text at (x, y) with a given scale (pixels per font pixel). */
	text(str: string, x: number, y: number, color: RGB, scale = 2): this {
		let cx = x;
		for (const ch of str) {
			const glyph = GLYPHS[ch];
			if (!glyph) { cx += (GLYPH_W + 1) * scale; continue; }
			for (let row = 0; row < GLYPH_H; row++) {
				const bits = glyph[row];
				for (let col = 0; col < GLYPH_W; col++) {
					if (bits & (0x10 >> col)) {
						this.rect(cx + col * scale, y + row * scale, scale, scale, color[0], color[1], color[2]);
					}
				}
			}
			cx += (GLYPH_W + 1) * scale;
		}
		return this;
	}

	/** Measure text width in pixels at a given scale. */
	measureText(str: string, scale = 2): number {
		return str.length * (GLYPH_W + 1) * scale;
	}

	/** Draw text centered horizontally. */
	textCentered(str: string, y: number, color: RGB, scale = 2): this {
		const tw = this.measureText(str, scale);
		return this.text(str, Math.floor((this.width - tw) / 2), y, color, scale);
	}

	/** Get the raw RGB24 frame buffer. */
	getBuffer(): Buffer {
		return this.buf;
	}
}

